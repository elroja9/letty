    private String validarCampos(){
    
        String msj="";
        if(txtCodigo.getText().equals("")){
        
            msj="Por favor ingrese el codigo de la categoría";
            txtCodigo.requestFocus();
            return msj;
        }
        
         if(txtNombre.getText().equals("")){
        
            msj="Por favor ingrese el nombre de la categoría";
            txtNombre.requestFocus();
            return msj;
        }
        
        
        return msj;
        
    }
