    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        String mensaje=validarCampos();

        if(mensaje.equals("")){
            if(nuevo==true){

                try {
                    // TODO add your handling code here:
                    categoria.setCod_categoria(Integer.parseInt(txtCodigo.getText()));
                    categoria.setNombre(txtNombre.getText());
                    BDCategoria.insertarCategoria(categoria);
                } catch (SQLException ex) {
                    Logger.getLogger(MantCategoria.class.getName()).log(Level.SEVERE, null, ex);
                }
                JOptionPane.showMessageDialog(rootPane,"Se ha registrado con éxito la nueva categoria","Registro con éxito",JOptionPane.PLAIN_MESSAGE);
                this.dispose();
            }
            else{
                try {
                    categoria.setCod_categoria(Integer.parseInt(txtCodigo.getText()));
                    categoria.setNombre(txtNombre.getText());
                    BDCategoria.modificarCategorias(categoria);
                } catch (SQLException ex) {
                    Logger.getLogger(MantCategoria.class.getName()).log(Level.SEVERE, null, ex);
                }
                JOptionPane.showMessageDialog(rootPane,"Se ha actualizado con éxito la categoria","Actualización con éxito",JOptionPane.PLAIN_MESSAGE);
                this.dispose();

            }
            txtCodigo.setText("");
            txtNombre.setText("");
        }
        else{JOptionPane.showMessageDialog(rootPane, mensaje,"ERROR",JOptionPane.ERROR_MESSAGE);}

    }//GEN-LAST:event_btnAceptarActionPerformed

