    private void txtNombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombreKeyTyped
        // TODO add your handling code here:
        char car=evt.getKeyChar();
        if(txtNombre.getText().length()>=25) evt.consume();
        if((car<'a' || car>'z') && (car<'A' || car>'Z'))  {evt.consume(); jLabel6.setText("Digite solo letras");
            jLabel6.setForeground(Color.red);}else{jLabel6.setForeground(Color.white);}
        if(txtNombre.getText().equals("")){jLabel6.setForeground(Color.white);}
    }//GEN-LAST:event_txtNombreKeyTyped

    private void txtCodigoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodigoKeyTyped
        // TODO add your handling code here:
        char car = evt.getKeyChar();
        if(txtCodigo.getText().length()>=7) evt.consume();
        if((car<'0' || car>'9')) { evt.consume(); jLabel7.setText("Digite solo números");
            jLabel7.setForeground(Color.red);}else{jLabel7.setForeground(Color.white);}
        if(txtCodigo.getText().equals("")){jLabel7.setForeground(Color.white);}
    }//GEN-LAST:event_txtCodigoKeyTyped

