    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelTblProductos = (DefaultTableModel) tblProductos.getModel(); //TableProducto es el nombre de mi tabla ;) 
        int fila=tblProductos.getSelectedRow();
        int cant=Integer.parseInt(tblProductos.getValueAt(fila, 0).toString());
        //double impuesto=Double.parseDouble(FrmProducto.tblProductos.getValueAt(dia, cant))
        codigo=Integer.parseInt(tblProductos.getValueAt(fila,1).toString());
        double subtotal=Double.parseDouble(tblProductos.getValueAt(fila,4).toString());
        double sub=Double.parseDouble(txtSubtotal.getText());
        double subF=sub-subtotal;
        double imp=obtenerImpuesto();
        double desImp=cant*imp;
        double iV=Double.parseDouble(txtImpuesto.getText().toString());
        iV=iV-desImp;
        txtSubtotal.setText(String.valueOf(subF));
        txtImpuesto.setText(String.valueOf(iV));
        double Neto=iV+subF;
        txtNeto.setText(String.valueOf(Neto));
        modelTblProductos.removeRow(tblProductos.getSelectedRow()); 


    }//GEN-LAST:event_btnRemoverActionPerformed

