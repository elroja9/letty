    public void obtenerNumFactura() {
    
        Connection conn=conexionBD.getConnection();
        try{
                       
            Statement stm=conn.createStatement();
            ResultSet rs=stm.executeQuery("select * from tb_factura");
            while(rs.next()){
            
                factura_NO=rs.getInt("factura_NO");
                factura_NO=factura_NO+1;
                lblFactura.setText(String.valueOf(factura_NO));
               
            }
            if(factura_NO==0){
            factura_NO=factura_NO+1;
            lblFactura.setText(String.valueOf(factura_NO));
            }   
            conn.close();
        }catch(Exception e){JOptionPane.showMessageDialog(rootPane, e);}
    }
    
