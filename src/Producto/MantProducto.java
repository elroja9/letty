    public void cargarCategorias(){
    
        
        Connection conn;
        try{
        
            conn=conexionBD.getConnection();
            Statement stm=conn.createStatement();
            ResultSet rs=stm.executeQuery("select * from tb_categoria");
            while(rs.next()){
                
                String categorias=rs.getString("nombre");
                comboCategoria.addItem(categorias);
                               
            }conn.close();
            }catch(SQLException sqlex){
            setTitle("Problema CargarDatos" +sqlex.getMessage());}
        
    }
    
