    private void txtPrecioCompraKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPrecioCompraKeyTyped
        // TODO add your handling code here:
         char car = evt.getKeyChar();

        if(txtPrecioCompra.getText().length()>=7) evt.consume();
        if((car<'0' || car>'9')) {evt.consume(); lblPrecioCompra.setText("Sólo números");
            lblPrecioCompra.setForeground(Color.red);}else{lblPrecioCompra.setForeground(Color.white);}
        if(txtPrecioCompra.getText().equals("")){lblPrecioCompra.setForeground(Color.white);}
        txtPrecioCompra.addKeyListener(new KeyAdapter() {
            @Override
            public void keyReleased(final KeyEvent e) {
                if (!txtPrecioCompra.getText().equals("")) {
                    calculaImpuesto_venta();
                }
            }
        });
    }//GEN-LAST:event_txtPrecioCompraKeyTyped

