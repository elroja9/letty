    private void txtDescripcionKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDescripcionKeyTyped
        // TODO add your handling code here:
        char car = evt.getKeyChar();

        if(txtDescripcion.getText().length()>=15) evt.consume();
        if((car<'a' || car>'z') && (car<'A' || car>'Z')) {evt.consume(); lblDescripcion.setText("Sólo letras");
            lblDescripcion.setForeground(Color.red);}else{lblDescripcion.setForeground(Color.white);}
        if(txtDescripcion.getText().equals("")){lblDescripcion.setForeground(Color.white);}
    }//GEN-LAST:event_txtDescripcionKeyTyped

    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed

        String mensaje=validarCampos();
        if(mensaje.equals("")){
            boolean exonerado=checkExonerado.isSelected();

            if(nuevo==true){

                try {
                    // TODO add your handling code here:
                    producto.setCod_producto(Integer.parseInt(txtCodigo.getText()));
                    producto.setDescripcion(txtDescripcion.getText());
                    producto.setCod_categoria(Integer.parseInt(lblCategoria.getText()));
                    producto.setCed_proveedor(lblProveedor.getText());
                    producto.setPrecio_compra(Double.parseDouble(txtPrecioCompra.getText()));
                    producto.setPrecio_venta(Double.parseDouble(txtPrecioVenta.getText()));
                    producto.setImpuesto(Double.parseDouble(txtImpuesto.getText()));
                    producto.setExonerado(exonerado);
                    producto.setCant(Integer.parseInt(txtCant.getText().toString()));
                    BDProducto.insertarProducto(producto);
                } catch (SQLException ex) {
                    Logger.getLogger(MantProducto.class.getName()).log(Level.SEVERE, null, ex);
                }
                JOptionPane.showMessageDialog(rootPane,"Se ha registrado con éxito el nuevo producto","Registro con éxito",JOptionPane.PLAIN_MESSAGE);
                this.dispose();
            }
            else{
                try {
                    producto.setCod_producto(Integer.parseInt(txtCodigo.getText()));
                    producto.setDescripcion(txtDescripcion.getText());
                    producto.setCod_categoria(Integer.parseInt(lblCategoria.getText()));
                    producto.setCed_proveedor(lblProveedor.getText());
                    producto.setPrecio_compra(Double.parseDouble(txtPrecioCompra.getText()));
                    producto.setPrecio_venta(Double.parseDouble(txtPrecioVenta.getText()));
                    producto.setImpuesto(Double.parseDouble(txtImpuesto.getText()));
                    producto.setExonerado(exonerado);
                    BDProducto.modificarProducto(producto);
                } catch (SQLException ex) {
                    Logger.getLogger(MantProducto.class.getName()).log(Level.SEVERE, null, ex);
                }
                JOptionPane.showMessageDialog(rootPane,"Se ha actualizado con éxito el cliente","Actualización con éxito",JOptionPane.PLAIN_MESSAGE);
                this.dispose();

            }
            txtCodigo.setText("");
            txtDescripcion.setText("");
            lblCategoria.setText("");
            txtPrecioCompra.setText("");
            txtPrecioVenta.setText("");
        }
        else{JOptionPane.showMessageDialog(rootPane, mensaje,"ERROR",JOptionPane.ERROR_MESSAGE);}
    }//GEN-LAST:event_btnAceptarActionPerformed

