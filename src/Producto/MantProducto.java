    public void calculaImpuesto_venta(){
    
        try {
        
            txtPrecioVenta.setEditable(false);
            precioCompra=Integer.parseInt(txtPrecioCompra.getText());
            precioVenta=precioCompra*0.25+precioCompra;
            impuesto=precioCompra*0.13;
            txtPrecioVenta.setText(String.valueOf(precioVenta));
            txtImpuesto.setText(String.valueOf(impuesto));
        }
        catch(Exception e){JOptionPane.showMessageDialog(rootPane, e);}
    }
    
