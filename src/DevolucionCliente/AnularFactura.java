    private void tblProductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProductosMouseClicked
        // TODO add your handling code here:
        
        
        tblProductos.addMouseListener(new MouseAdapter(){
            @Override
            public void mouseReleased(MouseEvent e){
                int fila = tblProductos.getSelectedRow();

                if(e.getClickCount()==2){
                    cantidadDisponible=Integer.parseInt(tblProductos.getValueAt(fila,0).toString());
                    codigo=Integer.parseInt(tblProductos.getValueAt(fila,1).toString());
                    if(cantidadDisponible>1){
                    
                        cantDevolver=Integer.parseInt(JOptionPane.showInputDialog(rootPane,"Cantidad de productos a devolver"));
                        if(cantDevolver>cantidadDisponible){
                        
                            JOptionPane.showMessageDialog(rootPane,"No se puede devolver una cantidad de productos que superan a los vendidos");
                        }
                        else{
                        
                            actualizarInventario();
                            frmFacturas.facturaNO=codigo;
                            frmFacturas.cargarDetalleFactura();
                        }
                    }
                    else{
                    
                        
                    }
                    }
                }
                
            }
       
        );
        
    }//GEN-LAST:event_tblProductosMouseClicked

