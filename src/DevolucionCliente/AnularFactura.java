    public void actualizarInventario(){
        Connection conn=conexionBD.getConnection();
        PreparedStatement ps=null;
        int cantInventario=0;
        boolean rpta=false;
        try{
            Statement stm=conn.createStatement();        
            ResultSet rs=stm.executeQuery("select * from tb_inventario where cod_producto='"+codigo+"'");
            while(rs.next()){
            
                cantInventario=rs.getInt("stock");
            }
            cantDevolver=cantDevolver+cantInventario;
            String call= "{CALL pa_modificar_inventario(?,?)}";
            ps=conn.prepareCall(call);
            ps.setInt(1, codigo);
            ps.setInt(2, cantDevolver);
            
            rpta=ps.executeUpdate()==1? true:false;
            }catch(SQLException sqlex){
            setTitle("Problema CargarDatos" +sqlex.getMessage());}
    }
